    @Override
    public void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        if (getArguments() != null) {
        }
    }

    @Override
    public View onCreateView(LayoutInflater inflater, ViewGroup container,
                             Bundle savedInstanceState) {
        // Inflate the layout for this fragment
        view = inflater.inflate(R.layout.fragment_settings, container, false);
        logoutButton = (Button) view.findViewById(R.id.settings_logout_button);
        userLabel = (TextView) view.findViewById(R.id.settings_username);
        userLabel.setText(userLabel.getText() + " " + MainActivity.user.getFullName());

        logoutButton.setOnClickListener(new View.OnClickListener() {

            @Override
            public void onClick(View v) {
                SharedPreferences prefs = getActivity().getApplication().getSharedPreferences("agendue", Context.MODE_PRIVATE);
                SharedPreferences.Editor e = prefs.edit();
                e.remove("username");
                e.remove("password");
                e.commit();
                Intent i = new Intent(getActivity().getBaseContext(), AgendueLogin.class);
                startActivity(i);
                getActivity().finish();
            }
        });
        return view;
    }

